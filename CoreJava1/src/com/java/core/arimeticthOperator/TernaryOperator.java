/*Java Ternary operator is used as one line replacement for if-then-else statement and used a 
 * lot in Java programming.
 * 
 * if(Teststatement?a:b)
 * If test statement is true then result is a
 * If test statement is flase then result is b
 */
package com.java.core.arimeticthOperator;

public class TernaryOperator 
{
	public static void main(String[] args) 
	{
			int a=12;  
			int b=5;  
			int min=(a<b)?a:b;  
			System.out.println(min);  
			
		
	}

}
